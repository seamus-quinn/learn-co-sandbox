def eating(food)
  if food == "candy"
    puts "dIAbeTeS"
    elsif food == "bread"
    puts "I love carbs"
    elsif food == "pizza"
    puts "cheese"
    elsif food == "ice cream"
    puts "with cake"
    elsif food == "fruit"
    puts "salad"
    elsif food == "cupcakes"
    puts "childhood favourite"
